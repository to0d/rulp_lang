package alpha.rulp.ximpl.rclass;

import alpha.rulp.lang.IRFrame;
import alpha.rulp.lang.IRList;
import alpha.rulp.lang.IRMember;
import alpha.rulp.lang.IRObject;
import alpha.rulp.lang.RException;
import alpha.rulp.runtime.IRFactor;
import alpha.rulp.runtime.IRInterpreter;
import alpha.rulp.utils.RulpFactory;
import alpha.rulp.utils.RuntimeUtil;
import alpha.rulp.ximpl.factor.AbsAtomFactorAdapter;

public class XRFactorAccess extends AbsAtomFactorAdapter implements IRFactor {

	public XRFactorAccess(String factorName) {
		super(factorName);
	}

	@Override
	public IRObject compute(IRList args, IRInterpreter interpreter, IRFrame frame) throws RException {

		if (args.size() != 2) {
			throw new RException("Invalid parameters: " + args);
		}

		IRObject obj = args.get(1);
		switch (obj.getType()) {
		case MEMBER:
			obj = RuntimeUtil.getActualMember((IRMember) obj, interpreter, frame);
			break;

		default:
		}

		return RulpFactory.createBoolean(RuntimeUtil.canAccess(obj, interpreter, frame));
	}

}
